    public void setCloseAction() {
        this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                int buttonClose = JOptionPane.YES_NO_OPTION;
                int result = JOptionPane.showConfirmDialog(rootPane, "Are you sure to exit?", "Confirm", buttonClose, JOptionPane.INFORMATION_MESSAGE);
                if (result == JOptionPane.YES_OPTION) {
                    System.exit(0);
                }
            }
        });
    }

