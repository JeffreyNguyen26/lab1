    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        // TODO add your handling code here:
        String day = txtDay.getText().trim();
        String month = txtMonth.getText().trim();
        String year = txtYear.getText().trim();
        
        ValidDate valid = new ValidDate();
        //Debug since 24/09/2018
//        boolean checkNull = valid.checkNull(day, month, year);
//        if (checkNull) {
            
            valid.checkDay(day);
            valid.checkMonth(month);
            valid.checkYear(year);
            List<DayMonthYearErr> result = valid.getListErr();
            if (result != null) {
                String listErr = "";
                for (int i = 0; i < result.size(); i++) {
                    listErr += result.get(i).getError() + "\n";
                }
                JOptionPane.showMessageDialog(null, listErr, "Error", JOptionPane.ERROR_MESSAGE);
            }else{
              boolean checkDate = valid.checkDate(day, month, year);
              if(checkDate){
                  JOptionPane.showMessageDialog(null, day + "/" + month + "/" + year + " is correct date time!", "Message", JOptionPane.INFORMATION_MESSAGE);
              }else{
                  JOptionPane.showMessageDialog(null, day + "/" + month + "/" + year + " is not correct date time!", "Message", JOptionPane.INFORMATION_MESSAGE);
              }
            }
//        }

    }//GEN-LAST:event_btnCheckActionPerformed

    
